# Base class
class Animal:
    def speak(self):
        return "Generic animal sound"

# Derived class overriding method
class Dog(Animal):
    def speak(self):
        return "Woof!"

# Derived class overriding method
class Cat(Animal):
    def speak(self):
        return "Meow!"

# Derived class not overriding method
class Cow(Animal):
    pass

# Creating instances and calling overridden methods
animal = Animal()
dog = Dog()
cat = Cat()
cow = Cow()

print(animal.speak())  # Output: Generic animal sound
print(dog.speak())     # Output: Woof!
print(cat.speak())     # Output: Meow!
print(cow.speak())     # Output: Generic animal sound