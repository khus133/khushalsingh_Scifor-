class Calculator:
    def add(self, a, b=0, c=0):
        return a + b + c

# Creating an instance of Calculator
calc = Calculator()

# Example of method overloading using default parameter values
print(calc.add(1))          # Output: 1
print(calc.add(1, 2))       # Output: 3
print(calc.add(1, 2, 3))    # Output: 6

